    if not img.endswith((".jpg",".jpeg",".png")):
        continue

    img_path = os.path.join(test_images, img)

    label_path = os.path.join(test_labels, img.rsplit(".",1)[0] + ".txt")

    # prediction
    results = model.predict(img_path, conf=0.25, verbose=False)

    pred_classes = []

    for r in results:
        if r.boxes is not None:
            pred_classes = r.boxes.cls.cpu().numpy().astype(int).tolist()

    # ground truth
    true_classes = []

    if os.path.exists(label_path):
        with open(label_path) as f:
            for line in f.readlines():
                true_classes.append(int(line.split()[0]))

    # match lengths
    min_len = min(len(true_classes), len(pred_classes))

    y_true.extend(true_classes[:min_len])
    y_pred.extend(pred_classes[:min_len])

