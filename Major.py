for img_name in os.listdir(test_images):

    if not img_name.endswith((".jpg",".jpeg",".png")):
        continue

    img_path = os.path.join(test_images, img_name)
    label_path = os.path.join(test_labels, img_name.rsplit(".",1)[0] + ".txt")

    # Read image
    img = cv2.imread(img_path)

    if img is None:
        continue

    img_rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
    img_tensor = F.to_tensor(img_rgb).to(device)

    # Prediction
    with torch.no_grad():
        outputs = model([img_tensor])

    pred_labels = outputs[0]['labels'].cpu().numpy()
    scores = outputs[0]['scores'].cpu().numpy()

    # Filter predictions (confidence)
    pred_filtered = [label for label, score in zip(pred_labels, scores) if score > 0.5]

    # Ground truth (YOLO labels)
    true_labels = []
    if os.path.exists(label_path):
        with open(label_path) as f:
            for line in f.readlines():
                cls = int(line.split()[0]) + 1  # +1 for COCO format
                true_labels.append(cls)

    # Match lengths
    min_len = min(len(true_labels), len(pred_filtered))

    y_true.extend(true_labels[:min_len])
    y_pred.extend(pred_filtered[:min_len])

print("Total Samples:", len(y_true))

